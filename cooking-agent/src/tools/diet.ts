export const diet_impl: ToolImpl<{ diet_type: string; dish_name?: string }> = async (args) => {
  const start = Date.now()
  const { diet_type, dish_name } = args

  const rule = DIET_RULES.find(
    (r) => r.type === diet_type || r.label.includes(diet_type),
  )

  if (!rule) {
    return {
      success: false,
      error: `暂不支持「${diet_type}」膳食模式。目前支持：生酮、低卡、高蛋白、素食、纯素、无麸质、低FODMAP、糖尿病友好。`,
      duration: Date.now() - start,
    }
  }

  const result: Record<string, unknown> = {
    diet_type: rule.type,
    label: rule.label,
    description: rule.description,
    forbidden: rule.forbidden,
    substitute_map: rule.substitute_map,
    tips: rule.tips,
  }

  if (dish_name) {
    result.dish_name = dish_name
    result.adaptation_guide = `请根据以上「${rule.label}」的禁忌清单和替代映射表，对「${dish_name}」的做法进行调整。重点替换 forbidden 清单中的食材，使用 substitute_map 中的替代品。`
  }

  return {
    success: true,
    data: result,
    duration: Date.now() - start,
  }
}